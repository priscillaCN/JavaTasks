public class ArrayRange {

	public static void main(String[] args) {
		
		
	int[] number = new int[] {2, 5, 7, 9, 20};

	int range = number [4] - number [0];
	
	System.out.println (range);
	
	System.out.println();
	
	number = new int[] {2, 3, 1, 4, 6};
	
	range = number [4] - number [2];
	
	System.out.print (range);
	}

}